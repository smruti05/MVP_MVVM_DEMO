package com.mvp_mvvm_demo.models;

public class UserDetailModel {
    private String userName, emailId, addStreet, addSuite, addCity, addZipCode;

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getEmailId() {
        return emailId;
    }

    public void setEmailId(String emailId) {
        this.emailId = emailId;
    }

    public String getAddStreet() {
        return addStreet;
    }

    public void setAddStreet(String addStreet) {
        this.addStreet = addStreet;
    }

    public String getAddSuite() {
        return addSuite;
    }

    public void setAddSuite(String addSuite) {
        this.addSuite = addSuite;
    }

    public String getAddCity() {
        return addCity;
    }

    public void setAddCity(String addCity) {
        this.addCity = addCity;
    }

    public String getAddZipCode() {
        return addZipCode;
    }

    public void setAddZipCode(String addZipCode) {
        this.addZipCode = addZipCode;
    }
}
